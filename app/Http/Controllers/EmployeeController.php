    {
        $query = Employee::query();
        $date      = $request->date_filter;
        switch($date){
            case 'today' :
                $query->whereDate('created_at', Carbon::today());
              break;
            case 'yesterday' :
                $query->whereDate('created_at', Carbon::yesterday());
              break;
        }
        $employees = $query->get();

        return view('index', compact('employees'));

    }

